export const compareObjectWithoutType = (obj1, obj2) => {
  if (!obj1 || !obj2 || Object.keys(obj1).length !== Object.keys(obj2).length) {
    return false;
  }
  const keys = Object.keys(obj1);
  let isEqual = true;
  keys.map((key) => {
    if (obj1[key] != obj2[key]) {
      isEqual = false;
    }
  });

  return isEqual;
};

