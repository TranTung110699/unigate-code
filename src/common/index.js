const cardinalNumbers = [
  '',
  'thousands',
  'millions',
  'billion',
  'trillions',
  'millions_of_billions',
];

const convert_number_to_words_with_a_set_of_3_numbers = (number = 0) => {
  if (!number) {
    return '';
  }
  let hundreds = parseInt(number / 100);
  let tens = parseInt((number % 100) / 10);
  let units = number % 10;

  if (hundreds) {
    hundreds = t(numberByWords[hundreds]);
  }
  if (tens) {
    const nub = tens * 10 + units;
    if (numberByWords[nub]) {
      tens = t(numberByWords[nub]);
      units = 0;
    } else {
      tens = t(numberByWords[tens * 10]);
    }
  }
  if (units) {
    units = t(`${[1, 5].includes(units) ? 'l_' : ''}${numberByWords[units]}`);
  }

  if (hundreds && tens && units) {
    return t('%s_hundred_%s_%s', [hundreds, tens, units]);
  } else if (hundreds && tens) {
    return t('%s_hundred_%s', [hundreds, tens]);
  } else if (hundreds && units) {
    return t('%s_hundred_and_%s', [hundreds, units]);
  } else if (hundreds) {
    return t('%s_hundred', [hundreds]);
  } else if (tens) {
    return units ? `${tens} ${units}` : tens;
  }

  return units;
};

export const amount_in_numbers_to_words = (amount) => {
  if (amount <= 0) {
    return t('zero');
  }
  let number = amount;

  const wordsSetOf3Numbers = [];
  let cardinalNumbersIndex = 0;
  while (number > 0) {
    const setOf3Numbers = number % 1000;

    if (setOf3Numbers) {
      wordsSetOf3Numbers.push(
        `${convert_number_to_words_with_a_set_of_3_numbers(setOf3Numbers)} ${t(
          cardinalNumbers[cardinalNumbersIndex],
        )}`,
      );
    }

    number = (number - setOf3Numbers) / 1000;
    cardinalNumbersIndex += 1;
  }

  return wordsSetOf3Numbers.reverse().join(',');
};
